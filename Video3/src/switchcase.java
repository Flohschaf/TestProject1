
public class switchcase {

	public static void main(String[] args) {
		
		String monat="01";
		
		switch(monat) {
		case "01": System.out.println("Januar");
		            break;
		case "02": System.out.println("Februar");   
		           break;
		default:   System.out.println("kein Monat");
		}

	}

}
